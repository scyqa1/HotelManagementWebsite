CREATE TABLE User(
         ID INT AUTO_INCREMENT UNIQUE, 
         Username VARCHAR(15) UNIQUE,
         Password VARCHAR(15),
         Name VARCHAR(15),
         Passport VARCHAR(30) NOT NULL UNIQUE,
         Email VARCHAR(15),
         Telephone INT
);

CREATE TABLE Room(
      RoomID INT UNIQUE NOT NULL,
      RoomType INT NOT NULL,
      IsSbIn INT NOT NULL,
      InPersonPassport VARCHAR(30) UNIQUE
);


CREATE TABLE UseRoom(
      RoomID INT NOT NULL,
      RoomType INT NOT NULL,
      IsBooked INT NOT NULL,
      BookPassport VARCHAR(30) NOT NULL,
      StartDate DATE NOT NULL,
      EndDate DATE NOT NULL
);

